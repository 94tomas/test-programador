     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $bystatus = $request['status'];

        $user = User::select()
            ->where(function($q) use($bystatus) {
                switch ($bystatus) {
                    case 'active':
                        $q->where('status', 1);
                        break;
                    case 'inactive':
                        $q->where('status', 0);
                        break;
                    default:
                        
                        break;
                }
            })
            ->paginate(10);

        return response()->json($user, 200);
    }

